package org.pio.helpz;

public class KeyPoint {
    private int widthX, heightY;

    public KeyPoint(int widthX, int heightY) {
        this.widthX = widthX;
        this.heightY = heightY;
    }

    public int getWidthX() {
        return widthX;
    }

    public int getHeightY() {
        return heightY;
    }
}
